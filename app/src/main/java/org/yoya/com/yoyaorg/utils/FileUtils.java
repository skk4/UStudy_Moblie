    /**
     * 保存文件
     *
     * @param pathToSave 保存文件的本地路径，包括文件名（绝对路径）
     * @param data       二进制byte数组
     */
    public static void saveFile(String pathToSave, byte[] data) throws Exception {
        File file = new File(pathToSave);
        if (file.exists()) {
            file.delete();
        }
        file.createNewFile();
        FileOutputStream fos = new FileOutputStream(file);
        fos.write(data);
        fos.close();
    }

    /**
     * 根据文件路径获取文件名
     * @param path
     * @return
     */
    public static String getFileNameByPath(String path) {
        int left = path.lastIndexOf("/");
        int right = path.lastIndexOf("\\");
        int index = left > right ? left : right;
        return path.substring(index + 1);
    }

    /**
     * 根据文件路径获取文件后缀
     * @param path
     * @return
     */
    public static String getFileSuffix(String path){
        if(path==null||path.equals(""))return null;


        int index = path.lastIndexOf(".")+1;
        String result = path.substring(index,path.length());
        return result;
    }
